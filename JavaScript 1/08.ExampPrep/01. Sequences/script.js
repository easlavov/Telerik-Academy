function Solve(params) {
    var N = parseInt(params[0]);
    var answer = 1;
    var first;
    var second;
    for (var i = 1; i < N; i++) {
        first = parseInt(params[i]);
        second = parseInt(params[i + 1]);
        if (first <= second) {
            continue;
        } else {
            answer += 1;
        }
    }

    return answer;
}

